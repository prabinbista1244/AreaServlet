package com.model;

public class Area {
	private int length;
	private int breadth;
	private int Aresult;
	
	
	public int getAresult() {
		return Aresult;
	}
	public void setAresult(int aresult) {
		Aresult = aresult;
	}
	public int getLength() {
		return length;
	}
	public void setLength(int length) {
		this.length = length;
	}
	public int getBreadth() {
		return breadth;
	}
	public void setBreadth(int breadth) {
		this.breadth = breadth;
	}
	
	

}
